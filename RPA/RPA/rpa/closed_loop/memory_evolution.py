"""
Memory Evolution - Track pattern origin, versions, failures, fixes, and usage.

This component provides a complete evolutionary history for patterns:
- Origin tracking (source, session, consolidation)
- Version history with change reasons
- Failure and fix records
- Usage statistics over time
- Pattern lineage (ancestors and descendants)
- Evolution events (mutation, merge, split)

This implements the Epic requirement for memory evolution that enables
the system to understand how patterns came to be and how they evolved.
"""

