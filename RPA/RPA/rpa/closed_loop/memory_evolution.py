@dataclass
class UsageSnapshot:
    """
    Snapshot of pattern usage at a point in time.

    Enables tracking of usage trends over time.
    """
    snapshot_id: str
    pattern_id: str
    timestamp: datetime = field(default_factory=datetime.now)

    # Usage counts
    total_uses: int = 0
    successful_uses: int = 0
    failed_uses: int = 0

    # Success metrics
    success_rate: float = 0.0
    average_score: float = 0.0

    # Reinforcement state
    strength: float = 1.0
    current_streak: int = 0

    # Rank among similar patterns
    domain_rank: Optional[int] = None
    global_rank: Optional[int] = None

    def __post_init__(self):
        """Calculate derived metrics."""
        if self.total_uses > 0 and self.success_rate == 0.0:
            self.success_rate = self.successful_uses / self.total_uses
        if self.average_score == 0.0:
            self.average_score = self.success_rate

    def to_dict(self) -> Dict[str, Any]:
        """Convert to dictionary."""
        return {
            "snapshot_id": self.snapshot_id,
            "pattern_id": self.pattern_id,
            "timestamp": self.timestamp.isoformat(),
            "total_uses": self.total_uses,
            "successful_uses": self.successful_uses,
            "failed_uses": self.failed_uses,
            "success_rate": self.success_rate,
            "average_score": self.average_score,
            "strength": self.strength,
            "current_streak": self.current_streak,
            "domain_rank": self.domain_rank,
            "global_rank": self.global_rank,
        }

    @classmethod
    def from_dict(cls, data: Dict[str, Any]) -> "UsageSnapshot":
        """Create from dictionary."""
        return cls(
            snapshot_id=data["snapshot_id"],
            pattern_id=data["pattern_id"],
            timestamp=datetime.fromisoformat(data["timestamp"]) if data.get("timestamp") else datetime.now(),
            total_uses=data.get("total_uses", 0),
            successful_uses=data.get("successful_uses", 0),
            failed_uses=data.get("failed_uses", 0),
            success_rate=data.get("success_rate", 0.0),
            average_score=data.get("average_score", 0.0),
            strength=data.get("strength", 1.0),
            current_streak=data.get("current_streak", 0),
            domain_rank=data.get("domain_rank"),
            global_rank=data.get("global_rank"),
        )


